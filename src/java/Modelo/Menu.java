package Modelo;

/**
 *
 * @author Administrador
 */
public class Menu {
    
    private int id;
    private String link;
    
    private String titulo;
    
    public Menu(){}

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getLink() {
        return link;
    }

    public void setLink(String link) {
        this.link = link;
    }

   

    public String getTitulo() {
        return titulo;
    }

    public void setTitulo(String titulo) {
        this.titulo = titulo;
    }
    
    
    
}
